# Demonstrating variables and data types in Python


# String
name = "Algorithms365"  # String value
print(f"String: name = {name}")


# Integer
age = 25  # Integer value
print(f"Integer: age = {age}")

# Float
height = 5.9  # Float value
print(f"Float: height = {height}")


# Boolean
is_active = True  # Boolean value
print(f"Boolean: is_active = {is_active}")

# Large Integer (can be used to represent long values)
large_number = 12345678901234567890  # Large integer value
print(f"Large Integer: large_number = {large_number}")




